package JavaDataStructures;

public class VideoGame {
    public String title;
    public int year;
    public String rating;
    public String[] platforms;



    public VideoGame(){}

    public VideoGame(String title, int year, String rating, String[] platforms) {
        this.title = title;
        this.year = year;
        this.rating = rating;
        this.platforms = platforms;
    }
}
